public class Escape {
 public static void main(String[] args){
  System.out.println(" \"quote starts like  and.() ends like \\\" ");
  System.out.println(" \"Hello");
  System.out.println(" \"Hello\" ");
  System.out.println(" H\"ell\"o ");
  System.out.println(" H\\ello ");
  System.out.println(" H\"ello\" ");
  System.out.println(" H\\ello ");
  System.out.println(" H\\\"ello "); 
  System.out.println(" H\\\"\"ello ");
  System.out.println(" H\\\"e\\\"llo ");
  System.out.println(" 'Hello' ");
  System.out.println(" H'ello' ");
  System.out.println(" H'ell'o ");
  System.out.println(" H'e\\ll\"o ");
  System.out.println(" Hello\tnivi ");
  System.out.println(" Hello\nnivi ");
  System.out.println(" Hello\rnivi ");
  System.out.println(" Hello\'nivi ");
  System.out.println(" Hello\"nivi ");
  System.out.println(" Hello\\nivi ");
 }
}